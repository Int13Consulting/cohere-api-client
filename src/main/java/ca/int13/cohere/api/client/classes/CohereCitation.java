package ca.int13.cohere.api.client.classes;

import com.google.gson.annotations.SerializedName;
import java.util.ArrayList;

/**
 *
 * @author mgamble
 */
public class CohereCitation {
    private int start;
    private int end;
    private String text;
    @SerializedName("document_ids")
    private ArrayList<String> documentIds;   

    /**
     * @return the start
     */
    public int getStart() {
        return start;
    }

    /**
     * @param start the start to set
     */
    public void setStart(int start) {
        this.start = start;
    }

    /**
     * @return the end
     */
    public int getEnd() {
        return end;
    }

    /**
     * @param end the end to set
     */
    public void setEnd(int end) {
        this.end = end;
    }

    /**
     * @return the text
     */
    public String getText() {
        return text;
    }

    /**
     * @param text the text to set
     */
    public void setText(String text) {
        this.text = text;
    }

    /**
     * @return the documentIds
     */
    public ArrayList<String> getDocumentIds() {
        return documentIds;
    }

    /**
     * @param documentIds the documentIds to set
     */
    public void setDocumentIds(ArrayList<String> documentIds) {
        this.documentIds = documentIds;
    }
}
